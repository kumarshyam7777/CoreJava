package com.durgasir.corejava.arrays;

public class Array_Example_4 {
    public static void main(String[] args) {
        //Jagged Array Declaration and Initialization
        int[] a1 = new int[0], b1 = new int[0];
        int[][] a = new int[0][], b = new int[0][];
        int[] a2[] = new int[0][], b2 = new int[0];
        int[][] a3 = new int[0][], b3[] = new int[0][][];
        System.out.println("");
        System.out.println(a1);
        System.out.println(b1);
        System.out.println(a);
        System.out.println(b);
        System.out.println(a2);
        System.out.println(b2);
        System.out.println(a3);
        System.out.println(b3);


    }
}
