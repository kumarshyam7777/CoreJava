package com.durgasir.corejava.arrays;

public class ArrayExample2 {

	public static void main(String[] args) {

		// array declaration.initialization and creation in a single line.(int array)
		int[] x = { 10, 20, 30, 40 };
		System.out.println(x);
		System.out.println(x[0]);
		System.out.println(x[1]);
		System.out.println(x[2]);
		System.out.println(x[3]);

		// array declaration.initialization and creation in a single line.(char array)
		char[] ch = { 'a', 'e', 'i', 'o', 'u' };
		System.out.println(ch);
		System.out.println(ch.toString());
		System.out.println(ch[0]);
		System.out.println(ch[1]);
		System.out.println(ch[2]);
		System.out.println(ch[3]);
		System.out.println(ch[4]);

		// array declaration.initialization and creation in a single line.(String Array)
		String[] s = { "Babu", "Rahul", "Riju", "Buni", "Shaan" };
		System.out.println(s);
		System.out.println(s.length);
		System.out.println(s.toString());
		System.out.println(s[0]);
		System.out.println(s[1]);
		System.out.println(s[2]);
		System.out.println(s[3]);
		System.out.println(s[4]);
		

	}

}
