package BasicMathOperations;

public class DataSizeRange {
    public static void main(String[] args) {

        // int data types min,max and bytes values
        System.out.println("Integer Min: " + Integer.MIN_VALUE);
        System.out.println("Integer Max: " + Integer.MAX_VALUE);
        System.out.println("Integer Bytes:" + Integer.BYTES);

        // byte data types in MIN,MAX and BYTES values
        System.out.println("short Min: " + Short.MIN_VALUE);
        System.out.println("Short Max:" + Short.MAX_VALUE);
        System.out.println("Short Bytes: " + Short.BYTES);

        //long data types in MIN,MAX and BYTES values
        System.out.println("long Min: " + Long.MIN_VALUE);
        System.out.println("long Max: " + Long.MAX_VALUE);
        System.out.println("long bytes: " + Long.BYTES);

        //double data types default values ,MIN,MAX,Bytes values
        System.out.println("double MIN: " + Double.MIN_VALUE);
        System.out.println("double MAX: " + Double.MAX_VALUE);
        System.out.println("double bytes: " + Double.BYTES);


    }
}
