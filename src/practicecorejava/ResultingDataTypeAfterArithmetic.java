package practicecorejava;

public class ResultingDataTypeAfterArithmetic {
    public static void main(String[] args) {
        // Arithmetic Operations over primitive data types
        byte b = 125;
        short s = 3654;
        int b1 = b + s;  // int = byte + short
        System.out.println(b1);
        System.out.println();
    }
}
