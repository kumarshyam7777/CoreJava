package typecasting;

public class TypeConversionSystemAuto_5 {
    public static void main(String[] args) {
        float f = 14214.2245f;
        double d = f;
        System.out.println("The double value type conversion from float to double:" + d);
        char ch = 'T';
        int i = ch;
        System.out.println("The integer value type conversion from char to int :" + i);
        long l = ch;
        System.out.println("The long value type conversion from char to long:" + l);
        float f1 = ch;
        System.out.println("The float value type conversion from char to float: " + f1);
        double d1 = ch;
        System.out.println("The double value type conversion from char to double:" + d1);


    }
}
