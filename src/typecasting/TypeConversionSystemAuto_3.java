package typecasting;

public class TypeConversionSystemAuto_3 {
    public static void main(String[] args) {
        int j = 1478564;
        long l = j;
        System.out.println("The long value type conversion from int to long:  + l");
        float f = j;
        System.out.println("The float value type conversion from int to float: " + f);
        double d = j;
        System.out.println("The double value type conversion from int to double:" + d);

    }
}
